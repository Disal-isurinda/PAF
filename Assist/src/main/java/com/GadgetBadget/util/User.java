package com.GadgetBadget.util;

public class User {
	private String 	tipId;
	private String relatedArea;
	private String tipDetail;
	private String date;
	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTipId() {
		return tipId;
	}
	public void setTipId(String tipId) {
		this.tipId = tipId;
	}
	public String getRelatedArea() {
		return relatedArea;
	}
	public void setRelatedArea(String relatedArea) {
		this.relatedArea = relatedArea;
	}
	public String getTipDetail() {
		return tipDetail;
	}
	public void setTipDetail(String tipDetail) {
		this.tipDetail = tipDetail;
	}
	
	
}
